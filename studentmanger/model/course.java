package model;

public class course {
    private String cname;
    private String id;
    private String cmark;
    public String getCname() {
        return cname;
    }
    public void setCname(String cname) {
        this.cname = cname;
    }
    public String getCmark() {
        return cmark;
    }
    public void setCmark(String cmark) {
        this.cmark = cmark;
    }
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }

}